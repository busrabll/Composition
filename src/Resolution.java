
public class Resolution {
	
	private int width;
	private int size;
	
	public Resolution(int width, int size) {

		this.width = width;
		this.size = size;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}	
}
